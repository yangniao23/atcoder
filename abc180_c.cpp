using namespace std;

vector<unsigned long long> divisor(long long n) {
    vector<unsigned long long> ret;
    for (unsigned long long i = 1; i * i <= n; i++) {
        if (n % i == 0) {
            ret.push_back(i);
            if (i * i != n) ret.push_back(n / i);
        }
    }
    sort(ret.begin(), ret.end()); // 昇順に並べる
    return ret;
}

signed main(void) {
    unsigned ll n;
    cin >> n;
    vector<unsigned long long> ans = divisor(n);
    copy(begin(ans), end(ans), ostream_iterator<int>(cout, "\n"));
    return 0;
}